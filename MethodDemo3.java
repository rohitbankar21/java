class MethodDemo3
{
	public static void main(String[] args)
	{
		addition(10,20,30);
		substraction(50,60,70);
	}
	static void addition(int a,int b,int d)
	{
		int c=0;
		c=a+b+d;
		System.out.println(c);
	}
	static void substraction(int a,int b,int d)
	{
		int c=0;
		c=a-b-d;
		System.out.println(c);
	}
}